package com.thinhnv.ungdunghinhhoc.hinh.dinh;

public class Dinh {
	private double toaDoX;
	private double toaDoY;

	public Dinh(double toaDoX, double toaDoY) {
		this.toaDoX = toaDoX;
		this.toaDoY = toaDoY;
	}

	public double getToaDoX() {
		return toaDoX;
	}

	public double getToaDoY() {
		return toaDoY;
	}
	

}
