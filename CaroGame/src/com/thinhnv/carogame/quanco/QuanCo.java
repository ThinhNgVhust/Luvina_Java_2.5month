package com.thinhnv.carogame.quanco;

public class QuanCo {
	private int x;
	private int y;
	private String loaiQuan;

	public QuanCo(int x, int y, String loaiQuan) {
		this.x = x;
		this.y = y;
		this.loaiQuan = loaiQuan;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public String getLoaiQuan() {
		return loaiQuan;
	}
	

}
